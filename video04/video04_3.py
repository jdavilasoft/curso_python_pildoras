# Saber el tipo de dato de una variable usando TYPE

# Enteros
numero = 666
print(type(numero))

# Flotantes
pi = 3.141592
print(type(pi))

# Texto
nombre = "Perico el de los palotes"
print(type(nombre))

# Boleano
verdad = True
print(type(verdad))

